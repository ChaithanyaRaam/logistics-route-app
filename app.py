# ==================================================
# GEO HELPERS
# ==================================================
def haversine(lat1, lon1, lat2, lon2):
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = (
        math.sin(dlat / 2) ** 2
        + math.cos(math.radians(lat1))
        * math.cos(math.radians(lat2))
        * math.sin(dlon / 2) ** 2
    )
    return 2 * R * math.asin(math.sqrt(a))


def bearing(lat1, lon1, lat2, lon2):
    return math.atan2(
        math.radians(lon2 - lon1),
        math.radians(lat2 - lat1)
    )

# ==================================================
# KML LOADER (LOCAL FILE â€“ BACKEND ONLY)
# ==================================================
def parse_kml_local(path):
    with open(path, "rb") as f:
        root = ET.fromstring(f.read())

    ns = {"kml": "http://www.opengis.net/kml/2.2"}
    rows = []

    for pm in root.findall(".//kml:Placemark", ns):
        name = pm.find("kml:name", ns)
        coord = pm.find(".//kml:coordinates", ns)

        if name is None or coord is None:
            continue

        lon, lat, *_ = coord.text.strip().split(",")

        rows.append({
            "Pincode": str(name.text).strip(),
            "Latitude": float(lat),
            "Longitude": float(lon)
        })

    if not rows:
        raise RuntimeError("KML loaded but no valid pincodes found")

    return pd.DataFrame(rows)

# ==================================================
# ZONE-AWARE ROUTING ENGINE
# ==================================================
def build_geo_routes(df, wh_lat, wh_lon, wh_name, plan_type):

    # Zone priority by warehouse
    if wh_name == "WH1":
        ZONE_ORDER = [
            "South / OMR / Tambaram",
            "Outer West / Peripheral"
        ]
    else:  # WH2
        ZONE_ORDER = [
            "Velachery / Guindy / Saidapet",
            "Central Chennai",
            "West / Inner West",
            "North Chennai"
        ]

    routes = []

    for biker_id in sorted(df["Biker_ID"].unique()):
        grp = df[df["Biker_ID"] == biker_id].copy()

        # Compute geo metrics
        grp["Bearing"] = grp.apply(
            lambda x: (math.degrees(
                bearing(wh_lat, wh_lon, x.Latitude, x.Longitude)
            ) + 360) % 360,
            axis=1
        )

        grp["Distance"] = grp.apply(
            lambda x: haversine(wh_lat, wh_lon, x.Latitude, x.Longitude),
            axis=1
        )

        ordered_chunks = []

        for zone in ZONE_ORDER:
            zdf = grp[grp["Zone"] == zone].copy()
            if zdf.empty:
                continue

            # ðŸ”‘ KEY FIX:
            # WH1 â†’ Distance first (closest â†’ far)
            # WH2 â†’ Bearing first (directional sweep)
            if wh_name == "WH1":
                zdf = zdf.sort_values(["Distance", "Bearing"])
            else:
                zdf = zdf.sort_values(["Bearing", "Distance"])

            ordered_chunks.append(zdf)

        if not ordered_chunks:
            continue

        final_df = pd.concat(ordered_chunks)

        # Start from warehouse
        seq = 1
        routes.append({
            "Warehouse": wh_name,
            "Biker_ID": biker_id,
            "Pincode": wh_name,
            "Latitude": wh_lat,
            "Longitude": wh_lon,
            "TotalOrders": 0,
            "Sequence": seq,
            "Plan_Type": plan_type
        })

        # Append delivery points
        for _, r in final_df.iterrows():
            seq += 1
            routes.append({
                "Warehouse": wh_name,
                "Biker_ID": biker_id,
                "Pincode": r["Pincode"],
                "Latitude": r["Latitude"],
                "Longitude": r["Longitude"],
                "TotalOrders": r["TotalOrders"],
                "Sequence": seq,
                "Plan_Type": plan_type
            })

    return pd.DataFrame(routes)

# ==================================================
# PLAN A â€“ CAPACITY BASED
# ==================================================
def plan_a(df, capacity, wh_lat, wh_lon, wh_name):
    assigned = []
    biker = 1
    load = 0

    for _, r in df.iterrows():
        if load + r.TotalOrders > capacity:
            biker += 1
            load = 0
        assigned.append({**r, "Biker_ID": biker})
        load += r.TotalOrders

    return build_geo_routes(pd.DataFrame(assigned), wh_lat, wh_lon, wh_name, "Plan A â€“ Capacity Based")

# ==================================================
# PLAN B â€“ RIDER COUNT BASED
# ==================================================
def plan_b(df, riders, wh_lat, wh_lon, wh_name):
    if riders <= 0:
        return pd.DataFrame()

    df = df.copy()
    df["Bearing"] = df.apply(
        lambda x: bearing(wh_lat, wh_lon, x.Latitude, x.Longitude),
        axis=1
    )
    df = df.sort_values("Bearing").reset_index(drop=True)

    chunk = math.ceil(len(df) / riders)
    assigned = []

    for i in range(riders):
        sub = df.iloc[i * chunk:(i + 1) * chunk]
        for _, r in sub.iterrows():
            assigned.append({**r, "Biker_ID": i + 1})

    return build_geo_routes(pd.DataFrame(assigned), wh_lat, wh_lon, wh_name, "Plan B â€“ Rider Based")

