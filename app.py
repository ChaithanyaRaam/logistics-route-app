# KML LOADER (LOCAL, BACKEND ONLY)
# ==================================================
def parse_kml_local(path):
    with open(path, "rb") as f:
        root = ET.fromstring(f.read())

    ns = {"kml": "http://www.opengis.net/kml/2.2"}
    rows = []

    for pm in root.findall(".//kml:Placemark", ns):
        name = pm.find("kml:name", ns)
        coord = pm.find(".//kml:coordinates", ns)

        if name is None or coord is None:
            continue

        lon, lat, *_ = coord.text.strip().split(",")

        rows.append({
            "Pincode": str(name.text).strip(),
            "Latitude": float(lat),
            "Longitude": float(lon)
        })

    if not rows:
        raise RuntimeError("KML loaded but no pincodes found")

    return pd.DataFrame(rows)

# ==================================================
# ROUTING ENGINE (STAGE â†’ DISTANCE â†’ BEARING)
# ==================================================
def build_geo_routes(df, wh_lat, wh_lon, wh_name, plan_type):

    routes = []

    for biker_id in sorted(df["Biker_ID"].unique()):
        grp = df[df["Biker_ID"] == biker_id].copy()

        grp["Bearing"] = grp.apply(
            lambda x: (math.degrees(
                bearing(wh_lat, wh_lon, x.Latitude, x.Longitude)
            ) + 360) % 360,
            axis=1
        )

        grp["Distance"] = grp.apply(
            lambda x: haversine(wh_lat, wh_lon, x.Latitude, x.Longitude),
            axis=1
        )

        # Assign stage
        if wh_name == WH1_NAME:
            grp["Stage"] = grp["Distance"].apply(wh1_stage)
        else:
            grp["Stage"] = grp["Distance"].apply(wh2_stage)

        # FINAL SORT RULE (LOCKED)
        final_df = grp.sort_values(
            ["Stage", "Distance", "Bearing"]
        )

        # Start at warehouse
        seq = 1
        routes.append({
            "Warehouse": wh_name,
            "Biker_ID": biker_id,
            "Pincode": wh_name,
            "Latitude": wh_lat,
            "Longitude": wh_lon,
            "TotalOrders": 0,
            "Sequence": seq,
            "Plan_Type": plan_type
        })

        for _, r in final_df.iterrows():
            seq += 1
            routes.append({
                "Warehouse": wh_name,
                "Biker_ID": biker_id,
                "Pincode": r["Pincode"],
                "Latitude": r["Latitude"],
                "Longitude": r["Longitude"],
                "TotalOrders": r["TotalOrders"],
                "Sequence": seq,
                "Plan_Type": plan_type
            })

    return pd.DataFrame(routes)

# ==================================================
# PLAN A â€“ CAPACITY BASED
# ==================================================
def plan_a(df, capacity, wh_lat, wh_lon, wh_name):
    assigned = []
    biker = 1
    load = 0

    for _, r in df.iterrows():
        if load + r.TotalOrders > capacity:
            biker += 1
            load = 0
        assigned.append({**r, "Biker_ID": biker})
        load += r.TotalOrders

    return build_geo_routes(pd.DataFrame(assigned), wh_lat, wh_lon, wh_name, "Plan A â€“ Capacity Based")

# ==================================================
# PLAN B â€“ RIDER COUNT BASED
# ==================================================
def plan_b(df, riders, wh_lat, wh_lon, wh_name):
    if riders <= 0:
        return pd.DataFrame()

    df = df.copy()
    df["Bearing"] = df.apply(
        lambda x: bearing(wh_lat, wh_lon, x.Latitude, x.Longitude),
        axis=1
    )
    df = df.sort_values("Bearing").reset_index(drop=True)

    chunk = math.ceil(len(df) / riders)
    assigned = []

    for i in range(riders):
        sub = df.iloc[i * chunk:(i + 1) * chunk]
        for _, r in sub.iterrows():
            assigned.append({**r, "Biker_ID": i + 1})

    return build_geo_routes(pd.DataFrame(assigned), wh_lat, wh_lon, wh_name, "Plan B â€“ Rider Based")

