def distance(lat1, lon1, lat2, lon2):
    return np.sqrt((lat1 - lat2) ** 2 + (lon1 - lon2) ** 2)

def build_routes(df, pickup_lat, pickup_lon, pickup_name, plan_type):
    routes = []

    for biker in sorted(df["Biker_ID"].unique()):
        group = df[df["Biker_ID"] == biker].to_dict("records")

        pickup = {
            "Warehouse": pickup_name,
            "Biker_ID": biker,
            "Pincode": pickup_name,
            "Latitude": pickup_lat,
            "Longitude": pickup_lon,
            "TotalOrders": 0
        }

        route = [pickup]
        current = pickup

        while group:
            nearest = min(
                group,
                key=lambda x: distance(
                    current["Latitude"], current["Longitude"],
                    x["Latitude"], x["Longitude"]
                )
            )
            route.append(nearest)
            group.remove(nearest)
            current = nearest

        for seq, stop in enumerate(route, start=1):
            stop["Sequence"] = seq
            stop["Plan_Type"] = plan_type
            routes.append(stop)

    return pd.DataFrame(routes)

# --------------------------------------------------
# PLAN A â€“ IDEAL CAPACITY BASED
# --------------------------------------------------
def plan_a(df, capacity_per_rider, pickup_lat, pickup_lon, pickup_name):
    assigned = []
    biker = 1
    used_capacity = 0

    for _, r in df.iterrows():
        if used_capacity + r["TotalOrders"] > capacity_per_rider:
            biker += 1
            used_capacity = 0

        assigned.append({
            "Warehouse": pickup_name,
            "Biker_ID": biker,
            "Pincode": r["Pincode"],
            "Latitude": r["Latitude"],
            "Longitude": r["Longitude"],
            "TotalOrders": r["TotalOrders"]
        })
        used_capacity += r["TotalOrders"]

    return build_routes(
        pd.DataFrame(assigned),
        pickup_lat,
        pickup_lon,
        pickup_name,
        "Ideal Capacity Based"
    )

# --------------------------------------------------
# PLAN B â€“ RIDER CONSTRAINED
# --------------------------------------------------
def plan_b(df, riders, pickup_lat, pickup_lon, pickup_name):
    assigned = []
    cycle = list(range(1, riders + 1))
    idx = 0

    for _, r in df.iterrows():
        assigned.append({
            "Warehouse": pickup_name,
            "Biker_ID": cycle[idx],
            "Pincode": r["Pincode"],
            "Latitude": r["Latitude"],
            "Longitude": r["Longitude"],
            "TotalOrders": r["TotalOrders"]
        })
        idx = (idx + 1) % riders

    result = build_routes(
        pd.DataFrame(assigned),
        pickup_lat,
        pickup_lon,
        pickup_name,
        "Rider Constrained"
    )

    load = (
        result[result["TotalOrders"] > 0]
        .groupby("Biker_ID")["TotalOrders"]
        .sum()
        .reset_index()
        .rename(columns={"TotalOrders": "Total_Load"})
    )

    return result.merge(load, on="Biker_ID", how="left")

