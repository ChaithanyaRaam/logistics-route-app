def wh1_stage(distance_km):
    """
    WH1 â€“ Chitlapakkam
    """
    if distance_km <= 7:
        return 1   # Inner Tambaram / Pallavaram
    elif distance_km <= 18:
        return 2   # OMR belt
    else:
        return 3   # Peripheral


def wh2_stage(distance_km):
    """
    WH2 â€“ Guindy (SIDCO)
    """
    if distance_km <= 6:
        return 1   # Core Guindy / Saidapet / Adyar
    elif distance_km <= 12:
        return 2   # Central Chennai
    elif distance_km <= 20:
        return 3   # West / Inner West
    else:
        return 4   # North Chennai

