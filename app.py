# ZONE-AWARE ROUTING (CORE FIX)
# ==================================================
def build_geo_routes(df, wh_lat, wh_lon, wh_name, plan_type):

    if wh_name == "WH1":
        ZONE_ORDER = [
            "South / OMR / Tambaram",
            "Outer West / Peripheral"
        ]
    else:  # WH2
        ZONE_ORDER = [
            "Velachery / Guindy / Saidapet",
            "Central Chennai",
            "West / Inner West",
            "North Chennai"
        ]

    routes = []

    for biker in sorted(df["Biker_ID"].unique()):
        grp = df[df["Biker_ID"] == biker].copy()

        grp["Bearing"] = grp.apply(
            lambda x: (math.degrees(
                bearing(wh_lat, wh_lon, x.Latitude, x.Longitude)
            ) + 360) % 360,
            axis=1
        )

        grp["Distance"] = grp.apply(
            lambda x: haversine(wh_lat, wh_lon, x.Latitude, x.Longitude),
            axis=1
        )

        ordered_chunks = []
        for zone in ZONE_ORDER:
            zdf = grp[grp["Zone"] == zone].copy()
            if zdf.empty:
                continue
            zdf = zdf.sort_values(["Bearing", "Distance"])
            ordered_chunks.append(zdf)

        if not ordered_chunks:
            continue

        final_df = pd.concat(ordered_chunks)

        seq = 1
        routes.append({
            "Warehouse": wh_name,
            "Biker_ID": biker,
            "Pincode": wh_name,
            "Latitude": wh_lat,
            "Longitude": wh_lon,
            "TotalOrders": 0,
            "Sequence": seq,
            "Plan_Type": plan_type
        })

        for _, r in final_df.iterrows():
            seq += 1
            routes.append({
                "Warehouse": wh_name,
                "Biker_ID": biker,
                "Pincode": r["Pincode"],
                "Latitude": r["Latitude"],
                "Longitude": r["Longitude"],
                "TotalOrders": r["TotalOrders"],
                "Sequence": seq,
                "Plan_Type": plan_type
            })

    return pd.DataFrame(routes)

